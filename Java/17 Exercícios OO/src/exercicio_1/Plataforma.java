package exercicio_1;

public class Plataforma {
	
	private String nomePlataforma;
	private int quantidadePlataforma = 1;
	
	public String getNomePlataforma() {
		return nomePlataforma;
	}
	public void setNomePlataforma(String nomePlataforma) {
		this.nomePlataforma = nomePlataforma;
	}
	public int getQuantidadePlataforma() {
		return quantidadePlataforma;
	}
	public void setQuantidadePlataforma(int quantidadePlataforma) {
		this.quantidadePlataforma = quantidadePlataforma;
	}
	
	
}
