package beans;

public class Produto {

	private int Celular;
	private int Notebook;
	private int TV;
	private int AparelhoSom;
	private int Guitarra;
	private int Violao;
	private int Bateria;
	private int Baixo;
	
	public int getCelular() {
		return Celular;
	}
	public void setCelular(int celular) {
		Celular = celular;
	}
	public int getNotebook() {
		return Notebook;
	}
	public void setNotebook(int notebook) {
		Notebook = notebook;
	}
	public int getTV() {
		return TV;
	}
	public void setTV(int tV) {
		TV = tV;
	}
	public int getAparelhoSom() {
		return AparelhoSom;
	}
	public void setAparelhoSom(int aparelhoSom) {
		AparelhoSom = aparelhoSom;
	}
	public int getGuitarra() {
		return Guitarra;
	}
	public void setGuitarra(int guitarra) {
		Guitarra = guitarra;
	}
	public int getViolao() {
		return Violao;
	}
	public void setViolao(int violao) {
		Violao = violao;
	}
	public int getBateria() {
		return Bateria;
	}
	public void setBateria(int bateria) {
		Bateria = bateria;
	}
	public int getBaixo() {
		return Baixo;
	}
	public void setBaixo(int baixo) {
		Baixo = baixo;
	}
}
